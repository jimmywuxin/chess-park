  /// 根据已走步数匹配开局库，返回下一步走法
  /// pace: 已走的所有步，格式 "fromRow fromCol toRow toCol" 用空格分隔
  static Move? getNextMove(List<Move> pace) {
    if (pace.isEmpty) return null;

    // 把已走步转成 itlwei 的编码格式：x1y1x2y2
    final paceStr = pace.map((m) {
      return '${m.from.col}${m.from.row}${m.to.col}${m.to.row}';
    }).join('');

    final matches = <String>[];
    for (final opening in _openings) {
      if (opening.startsWith(paceStr) && opening.length > paceStr.length) {
        matches.add(opening);
      }
    }

    if (matches.isEmpty) return null;

    // 随机选一个匹配的棋谱
    final chosen = matches[DateTime.now().millisecondsSinceEpoch % matches.length];
    final nextStep = chosen.substring(paceStr.length, paceStr.length + 4);

    final fromCol = int.parse(nextStep[0]);
    final fromRow = int.parse(nextStep[1]);
    final toCol = int.parse(nextStep[2]);
    final toRow = int.parse(nextStep[3]);

    return Move(Position(fromRow, fromCol), Position(toRow, toCol));
  }
